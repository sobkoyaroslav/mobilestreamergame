# Вспомогательные функции для русского текста (склонение по числам).

# Формы слов: (1 фолловер, 2 фолловера, 5 фолловеров).
FOLLOWERS = ("фолловер", "фолловера", "фолловеров")
STREAMS = ("стрим", "стрима", "стримов")


# Возвращает правильную форму слова для числа n.
# forms — кортеж из трёх форм: для 1, для 2 и для 5.
# Пример: plural(21, FOLLOWERS) -> 'фолловер'.
def plural(n, forms):
    n = abs(int(n))
    if n % 10 == 1 and n % 100 != 11:
        return forms[0]
    if 2 <= n % 10 <= 4 and not 12 <= n % 100 <= 14:
        return forms[1]
    return forms[2]


# Число с правильно склонённым словом «фолловер»: '5 фолловеров'.
def followers(n):
    n = int(n)
    return f"{n} {plural(n, FOLLOWERS)}"
